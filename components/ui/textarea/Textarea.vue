<script setup lang="ts">
import type { HTMLAttributes } from 'vue'
import { useVModel } from '@vueuse/core'
import { cn } from '@/lib/utils'

const props = defineProps<{
  class?: HTMLAttributes['class']
  defaultValue?: string | number
  modelValue?: string | number
}>()

const emits = defineEmits<{
  (e: 'update:modelValue', payload: string | number): void
}>()

const modelValue = useVModel(props, 'modelValue', emits, {
  passive: true,
  defaultValue: props.defaultValue,
})
</script>

<template>
  <textarea
    v-model="modelValue"
    data-slot="textarea"
    :class="cn('placeholder:text-black text-black focus-visible:border-black text-base aria-invalid:ring-destructive/20 dark:aria-invalid:ring-0 aria-invalid:border-black dark:bg-input/30 flex field-sizing-content min-h-[229px] w-full border-x-0  bg-transparent pl-0 lg:pl-4 py-5 text-f-lg transition-[color,box-shadow] outline-none focus-visible:ring-0 disabled:cursor-not-allowed disabled:opacity-50', props.class)"
  />
</template>
